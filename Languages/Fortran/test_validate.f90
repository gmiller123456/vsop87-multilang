    subroutine check2(a,b)
        double precision :: a
        double precision :: b
        double precision :: t1
        double precision :: t2
        double precision :: PI

        PI = 3.14159265358979d0

        t1 = mod(a,2*PI)
        t2 = mod(b,2*PI)

        if (t1 .lt. 0.0d0) t1 = t1 + 2*PI
        if (t2 .lt. 0.0d0) t2 = t2 + 2*PI

        if (abs(t1-t2) .gt. .0000000009d0) then
            !printf("Fail: %2.15f %2.15f\r\n",a,b);
            print *, 'Fail'
            print *, a
            print *, b
        else
            !print *, 'Pass'
        end if

    end subroutine

