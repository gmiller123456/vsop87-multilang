check2 <- function(a, b)
{
    t1 = a %% pi;
    t2 = b %% pi;

    if (t1 < 0) {
        t1 = t1 + pi;
    }
    if (t2 < 0) {t2 = t2 + pi;}

    if (abs(t1-t2) > .0000000009)
    {
        print(paste("Fail:",toString(a)," ",toString(b)));
        pass=0;
    } else
    {
        print("Pass");
    }
}

